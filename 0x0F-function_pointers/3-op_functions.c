#include "3-calc.h"

/**
  * op_add - addition operation
  * @a: First number
  * @b: Second number
  * Return: the solution which is an integer
  */
int op_add(int a, int b)
{
	return (a + b);
}

/**
  * op_sub - addition operation
  * @a: First number
  * @b: Second number
  * Return: the solution which is an integer
  */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
  * op_mul - addition operation
  * @a: First number
  * @b: Second number
  * Return: the solution which is an integer
  */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
  * op_div - addition operation
  * @a: First number
  * @b: Second number
  * Return: the solution which is an integer
  */
int op_div(int a, int b)
{
	return (a / b);
}

/**
  * op_mod - addition operation
  * @a: First number
  * @b: Second number
  * Return: the solution which is an integer
  */
int op_mod(int a, int b)
{
	return (a % b);
}
